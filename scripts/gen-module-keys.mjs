#!/usr/bin/env node
// Generate api/_lib/tangerineModules.js from the canonical MODULES array in
// src/Tangerine.tsx. The RBAC "User Access" panel reads the module list from
// the DB table `module_keys`; that table is a THIRD registry nobody updated
// when adding nav modules, so it drifted (33 rows vs 118 nav modules).
//
// This script makes Tangerine.tsx the single source of truth: it extracts
// every { key, label, group } and emits a server-importable mirror. The
// users-access handler unions this mirror with the DB table so the panel
// ALWAYS shows every menu item, and scripts/seed-module-keys.mjs upserts it
// into the DB so role grants can attach.
//
// Run:  node scripts/gen-module-keys.mjs   (also wired as npm run gen:module-keys)

import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

// Transactional groups support post/void; everything else is read/write/export.
const TXN_GROUPS = new Set([
  "Accounting", "Sales", "Vendors", "Customers", "Customers – Accts Rec",
  "Procurement", "Treasury", "Inventory", "Approvals", "Workflow",
]);
const actionsFor = (group) =>
  TXN_GROUPS.has(group)
    ? ["read", "write", "post", "void", "export"]
    : ["read", "write", "export"];

// The MODULES array moved from src/Tangerine.tsx to src/erp/modules.ts (nav
// refactor); this generator silently rotted pointing at the old file, freezing
// the api/_lib/tangerineModules.js mirror at 123 modules (~#1493).
const src = readFileSync(resolve(ROOT, "src/erp/modules.ts"), "utf8");
const start = src.indexOf("const MODULES: ModuleDef[] = [");
if (start === -1) { console.error("✗ MODULES array not found in src/erp/modules.ts"); process.exit(1); }
const end = src.indexOf("];", start);
const block = src.slice(start, end);

const re = /\{\s*key:\s*"([^"]+)",\s*label:\s*"([^"]+)",\s*emoji:\s*"([^"]*)",\s*group:\s*"([^"]+)"\s*\}/g;
const mods = [];
const seen = new Set();
let m;
while ((m = re.exec(block)) !== null) {
  const [, key, label, , group] = m;
  if (seen.has(key)) { console.error(`✗ duplicate module key: ${key}`); process.exit(1); }
  seen.add(key);
  mods.push({ key, display_name: label, group_name: group, available_actions: actionsFor(group) });
}
if (mods.length < 100) { console.error(`✗ only parsed ${mods.length} modules — parse likely broke`); process.exit(1); }

const rows = mods
  .map((x, i) => `  { key: ${JSON.stringify(x.key)}, display_name: ${JSON.stringify(x.display_name)}, group_name: ${JSON.stringify(x.group_name)}, sort_order: ${i}, available_actions: ${JSON.stringify(x.available_actions)} },`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-module-keys.mjs — DO NOT EDIT BY HAND.
// Source of truth: the MODULES array in src/Tangerine.tsx.
// Re-run \`node scripts/gen-module-keys.mjs\` after adding/removing a nav module.
//
// The RBAC User Access panel (api/internal/users-access) unions this list with
// the module_keys DB table so the grid always shows every Tangerine menu item.

export const TANGERINE_MODULES = [
${rows}
];

export default TANGERINE_MODULES;
`;

writeFileSync(resolve(ROOT, "api/_lib/tangerineModules.js"), out);
console.log(`✓ wrote api/_lib/tangerineModules.js with ${mods.length} modules`);
