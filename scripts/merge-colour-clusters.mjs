#!/usr/bin/env node
// Colour-cluster merge (step D of the colour normalization).
//
// A "cluster" is one physical colourway of one style that the catalog carries
// under SEVERAL spellings — `Black Paradise` / `Blck Paradise` on RYB0991. The
// spellings split ATS lines, on-hand, sales and cost. Clusters are found with
// `colorMatchKey` (the COLOR_ABBR dictionary), which is the same key the SKU
// create paths now match on (#1922), so this merge and the ingest guards agree.
//
// TWO distinct operations fall out of a cluster:
//
//   RENAME  — a SKU whose (canonical size, inseam) is unique inside the cluster.
//             Nothing to merge; it just needs `color` rewritten to the canonical
//             name. This alone collapses the duplicate ATS lines.
//   MERGE   — two or more SKUs in the cluster share the SAME (canonical size,
//             inseam): genuinely duplicate rows. One survives, the others are
//             retired and every FK child row is repointed to the survivor.
//
// Survivor rule (CEO, 2026-07-23): keep the SKU holding the most sales history
// (fewest repoints), then the oldest — but OVERWRITE its colour with the
// canonical name. "Keep the SKU with sales" alone would normalize onto the WRONG
// spelling, because on RYB0991 the sales sit on a row whose code is abbreviated.
//
// Canonical name (CEO, 2026-07-23): GENERATED by expanding every abbreviation,
// not selected from the existing spellings — PLAIN WORDS, NO PUNCTUATION, Title
// Case, single spaces. `Crumble-Md Blu` → `Crumble Medium Blue`.
//
// Default is DRY RUN: prints the plan and writes a pre-image manifest. Nothing
// is written to the database without --apply.
//
//   node scripts/merge-colour-clusters.mjs                 # dry run + manifest
//   node scripts/merge-colour-clusters.mjs --style=RYB0991 # scope to one style
//   node scripts/merge-colour-clusters.mjs --apply         # execute
//
