#!/usr/bin/env node
// Generates api/_handlers/routes.js — static imports + ordered routing
// table derived from the file system under api/_handlers/.
//
// Each handler's file path maps to a URL pattern:
//   api/_handlers/internal/onboarding/index.js            → /api/internal/onboarding
//   api/_handlers/internal/onboarding/[vendor_id]/index.js → /api/internal/onboarding/:vendor_id
//   api/_handlers/internal/compliance/[id]/review.js      → /api/internal/compliance/:id/review
//   api/_handlers/vendor/workspaces/[id]/tasks/[task_id].js → /api/vendor/workspaces/:id/tasks/:task_id
//
// Special files:
//   __tests__/**  → excluded
//   routes.js     → excluded (this file)

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..", "api", "_handlers");

function walk(dir, out = []) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    if (e.name === "__tests__") continue;
    if (e.name === "routes.js") continue;
    const full = path.join(dir, e.name);
    if (e.isDirectory()) walk(full, out);
    else if (e.isFile() && e.name.endsWith(".js")) out.push(full);
  }
  return out;
}

function pathToRoute(filePath) {
  // Strip ROOT prefix, strip trailing .js, strip trailing /index
  let rel = path.relative(ROOT, filePath).replace(/\\/g, "/");
  rel = rel.replace(/\.js$/, "");
  if (rel.endsWith("/index")) rel = rel.slice(0, -"/index".length);
  // Convert [name] to :name
  const segments = rel.split("/").map((seg) => {
    const dynMatch = seg.match(/^\[(\.\.\.)?(\w+)\]$/);
    if (dynMatch) {
      return dynMatch[1] === "..." ? `*${dynMatch[2]}` : `:${dynMatch[2]}`;
    }
    return seg;
  });
  const urlPath = "/api/" + segments.filter(Boolean).join("/");
  return urlPath || "/api";
}

function specificityKey(route) {
  // Higher = more specific = match first
  const segs = route.split("/").filter(Boolean);
  const nonParam = segs.filter((s) => !s.startsWith(":") && !s.startsWith("*")).length;
  const hasCatchall = segs.some((s) => s.startsWith("*"));
  // Prefer: no catchall, more non-param segments, longer path overall
  return [hasCatchall ? 0 : 1, nonParam, segs.length, route.length];
}

const files = walk(ROOT);
const entries = files.map((f) => ({ file: f, route: pathToRoute(f) }));
entries.sort((a, b) => {
  const ka = specificityKey(a.route);
  const kb = specificityKey(b.route);
  for (let i = 0; i < ka.length; i++) {
    if (ka[i] !== kb[i]) return kb[i] - ka[i];
  }
  return 0;
});

const lines = [];
lines.push("// Auto-generated by scripts/generate-api-routes.mjs — do not edit by hand.");
lines.push("// Every handler under api/_handlers/ is imported here so the single");
lines.push("// api/[[...path]].js dispatcher function bundles them all.");
lines.push("");

const importNames = new Map();
for (let i = 0; i < entries.length; i++) {
  const name = `h${i}`;
  importNames.set(entries[i].file, name);
  const relImport = "./" + path.relative(ROOT, entries[i].file).replace(/\\/g, "/");
  lines.push(`import ${name} from "${relImport}";`);
}

lines.push("");
lines.push("export const ROUTES = [");
for (const e of entries) {
  const name = importNames.get(e.file);
  lines.push(`  { pattern: ${JSON.stringify(e.route)}, handler: ${name} },`);
}
lines.push("];");
lines.push("");

// Also export a helper that compiles the regex lazily
lines.push(`export function compileRoutes(routes) {`);
lines.push(`  return routes.map((r) => {`);
lines.push(`    const segs = r.pattern.split("/").filter(Boolean);`);
lines.push(`    const params = [];`);
lines.push(`    const regexParts = segs.map((seg) => {`);
lines.push(`      if (seg.startsWith(":")) { params.push(seg.slice(1)); return "([^/]+)"; }`);
lines.push(`      if (seg.startsWith("*")) { params.push(seg.slice(1)); return "(.+)"; }`);
lines.push(`      return seg.replace(/[.*+?^\${}()|[\\]\\\\]/g, "\\\\$&");`);
lines.push(`    });`);
lines.push(`    const regex = new RegExp("^/" + regexParts.join("/") + "/?$");`);
lines.push(`    return { ...r, regex, params };`);
lines.push(`  });`);
lines.push(`}`);
lines.push("");

fs.writeFileSync(path.join(ROOT, "routes.js"), lines.join("\n"));
console.log(`Generated routes.js with ${entries.length} routes.`);
