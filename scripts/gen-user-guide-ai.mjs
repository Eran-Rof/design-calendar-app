// scripts/gen-user-guide-ai.mjs
//
// Generates api/_lib/ai/userGuideContent.js — a bundled, statically-importable
// snapshot of the Tangerine user guide so the Ask AI "search_user_guide" tool
// can read it at runtime (Vercel serverless functions can't reliably read loose
// docs/ files, so we inline them as an ESM module).
//
// Re-run after editing the guide:  node scripts/gen-user-guide-ai.mjs
// (Keep this in sync the same way menuKeys.js is kept in sync.)

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const GUIDE_DIR = join(ROOT, "docs", "tangerine", "user-guide");
const OUT = join(ROOT, "api", "_lib", "ai", "userGuideContent.js");

const files = readdirSync(GUIDE_DIR)
  .filter((f) => f.endsWith(".md"))
  .sort();

const chapters = [];
for (const file of files) {
  const raw = readFileSync(join(GUIDE_DIR, file), "utf8");
  // Title = first `# ` heading, else a humanised filename.
  const m = raw.match(/^#\s+(.+)$/m);
  const title = m ? m[1].trim() : file.replace(/\.md$/, "").replace(/[-_]/g, " ");
  // Light trim: collapse 3+ blank lines, drop nothing else (keep headings so the
  // searcher can section-split on ## / ###).
  const text = raw.replace(/\n{3,}/g, "\n\n").trim();
  chapters.push({ file, title, text });
}

const header = `// AUTO-GENERATED by scripts/gen-user-guide-ai.mjs — DO NOT EDIT BY HAND.
// Snapshot of docs/tangerine/user-guide/ for the Ask AI "search_user_guide" tool.
// Re-run \`node scripts/gen-user-guide-ai.mjs\` after editing the guide.
`;
const body = `export const USER_GUIDE = [\n${chapters
  .map((c) => `  { file: ${JSON.stringify(c.file)}, title: ${JSON.stringify(c.title)}, text: ${JSON.stringify(c.text)} },`)
  .join("\n")}\n];\n`;

writeFileSync(OUT, header + "\n" + body, "utf8");
const bytes = Buffer.byteLength(header + body, "utf8");
console.log(`Wrote ${OUT}: ${chapters.length} chapters, ${(bytes / 1024).toFixed(0)} KB`);
